package SampleIF;

public class SampleIF {

	public static void main(String[] args) {
		
		int number=27;
		
		if (number == 27) {
			System.out.println("is true");
		}
		
		else {
			System.out.println("is false");
		}
		
		int first=1;
		int second=3;
		
		boolean isLesser = first < second;
		
		if(isLesser) {
			System.out.println(first + "is less than " + second + "!");
			
		}

	}

}
